class Dog:
    pinzhong = ''
    name = ''
    def chi(self):
        print(' %s汪汪汪'%(self.pingzhong))
    def jiao(self):
        print('%s吃狗粮'%(self.name))
dog1 = Dog()
dog1.name = '旺财'
dog1.pingzhong = '拉布拉多'
dog1.chi()
dog1.jiao()
dog2 = Dog()
dog2.name = '地毯'
dog2.pingzhong = '京巴'
dog2.chi()
dog2.jiao()



